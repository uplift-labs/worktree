OPENCODE_ADAPTER_DIR="$INSTALL_ROOT/adapters/opencode"
mkdir -p "$OPENCODE_ADAPTER_DIR/plugins" "$OPENCODE_ADAPTER_DIR/tui"
rm -rf "${OPENCODE_ADAPTER_DIR:?}/bin" "${OPENCODE_ADAPTER_DIR:?}/lib"
printf '[install] copying OpenCode adapter to %s\n' "$OPENCODE_ADAPTER_DIR"
sync_js_dir "$SCRIPT_DIR/adapters/opencode/plugins" "$OPENCODE_ADAPTER_DIR/plugins"
sync_js_tsx_dir "$SCRIPT_DIR/adapters/opencode/tui" "$OPENCODE_ADAPTER_DIR/tui"

OPENCODE_PROJECT_PLUGIN_DIR="$TARGET/.opencode/plugins"
mkdir -p "$OPENCODE_PROJECT_PLUGIN_DIR"
printf '[install] writing OpenCode plugin to %s\n' "$OPENCODE_PROJECT_PLUGIN_DIR/worktree-sandbox.js"
cp "$SCRIPT_DIR/adapters/opencode/plugins/worktree-sandbox.js" \
   "$OPENCODE_PROJECT_PLUGIN_DIR/worktree-sandbox.js" || {
  printf 'install: copy failed %s -> %s\n' \
    "$SCRIPT_DIR/adapters/opencode/plugins/worktree-sandbox.js" \
    "$OPENCODE_PROJECT_PLUGIN_DIR/worktree-sandbox.js" >&2
  exit 1
}

OPENCODE_TUI_CONFIG="$TARGET/.opencode/tui.json"
OPENCODE_TUI_PLUGIN_DIR="$TARGET/.opencode/tui-plugins"
printf '[install] writing OpenCode TUI plugin to %s\n' "$OPENCODE_TUI_PLUGIN_DIR"
mkdir -p "$OPENCODE_TUI_PLUGIN_DIR"
for _OPENCODE_TUI_FILE in "$SCRIPT_DIR/adapters/opencode/tui"/*.js "$SCRIPT_DIR/adapters/opencode/tui"/*.tsx; do
  [ -e "$_OPENCODE_TUI_FILE" ] || continue
  cp "$_OPENCODE_TUI_FILE" "$OPENCODE_TUI_PLUGIN_DIR/" || {
    printf 'install: copy failed %s -> %s\n' "$_OPENCODE_TUI_FILE" "$OPENCODE_TUI_PLUGIN_DIR" >&2
    exit 1
  }
done

printf '[install] adding OpenCode TUI branch plugin to %s\n' "$OPENCODE_TUI_CONFIG"
merge_opencode_tui_plugin "$OPENCODE_TUI_CONFIG" "./tui-plugins/worktree-sandbox-branch.tsx"

if [ "$WITH_OPENCODE_PERMISSIONS" -eq 1 ]; then
  OPENCODE_CONFIG="$TARGET/opencode.json"
  printf '[install] adding conservative OpenCode permissions to %s\n' "$OPENCODE_CONFIG"
  merge_opencode_permissions "$OPENCODE_CONFIG"
fi

if [ "$WITH_OPENCODE_OS_SANDBOX" -eq 1 ]; then
  OPENCODE_CONFIG="$TARGET/opencode.json"
  printf '[install] adding opencode-sandbox plugin to %s\n' "$OPENCODE_CONFIG"
  merge_opencode_plugin "$OPENCODE_CONFIG" "opencode-sandbox"
fi

printf '[install] done.\n'
printf '  core installed at: %s\n' "$INSTALL_ROOT/core"
printf '  pre-merge-commit hook: %s\n' "$HOOK"
printf '  post-merge hook: %s\n' "$POST_MERGE"
printf '  opencode adapter: %s\n' "$INSTALL_ROOT/adapters/opencode"
[ "$WITH_OPENCODE_PERMISSIONS" -eq 1 ] && printf '  opencode permissions: %s\n' "$TARGET/opencode.json"
[ "$WITH_OPENCODE_OS_SANDBOX" -eq 1 ] && printf '  opencode OS sandbox plugin: %s\n' "$TARGET/opencode.json"
exit 0
